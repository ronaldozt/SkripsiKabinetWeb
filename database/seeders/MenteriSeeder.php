        $takeId = function ($byKode, $kode, $fallbackKode = 0) {
            if (isset($byKode[$kode])) {
                return $byKode[$kode]->id;
            }
            if (isset($byKode[$fallbackKode])) {
                return $byKode[$fallbackKode]->id;
            }
            throw new \Exception("Master kode_umap {$kode} tidak ditemukan dan fallback {$fallbackKode} juga tidak ada.");
        };

